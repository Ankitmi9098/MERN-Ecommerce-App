export const categoryBasedProductController = async (req, res) => {
  try {
    const category = await categoryModel.findOne({ slug: req.params.slug });
    if (category) {
      const products = await productModel
        .find({ category: category?._id })
        .select("-photo")
        .populate("category");
      if (products) {
        res.status(200).send({
          success: true,
          products,
          category,
        });
      }
    } else {
      res.status(200).send({
        success: false,
        message: `No category found with slug - ${req.params.slug} `,
      });
    }
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      message: error.message,
    });
  }
};

